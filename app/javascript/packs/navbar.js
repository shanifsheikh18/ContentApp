$(document).ready(function(){
    $(".on").click(function() {
        $('.nav-alert').css({
            'transform': 'translateX(0)',
        });
    });
    
    $(".close").click(function() {
        $('.nav-alert').css({
            'transform': 'translateX(150%)',
        });
    });

    $(".out").click(function(){
        $('.nav-alert').css({
            'transform': 'translateX(150%)',
        });
    })
})

